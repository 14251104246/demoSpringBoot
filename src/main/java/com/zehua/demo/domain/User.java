package com.zehua.demo.domain;

import java.io.Serializable;

public class User implements Serializable{//2级缓存时序列化
    private static final long serialVersionUID = 1690366993414489696L;

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
